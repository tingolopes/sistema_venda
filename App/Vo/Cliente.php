<?php

namespace App\Vo;

class Cliente
{
    const INATIVO = 0;
    const ATIVO = 1;

    private $idcliente;
    private $nome;
    private $email;
    private $ativo;

    public static function fromArray($array)
    {
        $cliente = new Cliente();
        $cliente->setIdcliente();
        $cliente->setNome();
        $cliente->setEmail();
        $cliente->setAtivo();

        return $cliente;
    }

    /**
     * @return mixed
     */
    public function getIdcliente()
    {
        return $this->idcliente;
    }

    /**
     * @param mixed $idcliente
     */
    public function setIdcliente($idcliente)
    {
        $this->idcliente = $idcliente;
    }

    /**
     * @return mixed
     */
    public function getNome()
    {
        return $this->nome;
    }

    /**
     * @param mixed $nome
     */
    public function setNome($nome)
    {
        $this->nome = $nome;
    }

    /**
     * @return mixed
     */
    public function getEmail()
    {
        return $this->email;
    }

    /**
     * @param mixed $email
     */
    public function setEmail($email)
    {
        $this->email = $email;
    }

    /**
     * @return mixed
     */
    public function getAtivo()
    {
        return $this->ativo;
    }

    /**
     * @param mixed $ativo
     */
    public function setAtivo($ativo)
    {
        $this->ativo = $ativo;
    }


}